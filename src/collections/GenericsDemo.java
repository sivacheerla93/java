package collections;

import java.util.ArrayList;
import java.util.Collections;

public class GenericsDemo {

	public static void main(String[] args) {

		ArrayList<String> list = new ArrayList<>();
		list.add("Java");
		list.add("C");
		list.add("String");

		Collections.sort(list);

		for (String s : list)
			System.out.println(s);
	}

}